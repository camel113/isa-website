        <p>In 2015 I created a WORLD MAP OF HEROINES, a kind of work in progress and interactive artwork involving the public. This work focuses on women who have contributed to the improvement of the world. I have sewn the five continents on a blank canvas and people are invited to sew beads onto the canvas to mark the place where THEIR heroine is located. They can also write an explanation about why this person is their heroine. The aim is to illuminate the map of the world with beads, each one representing a sign of hope and encouragement to others.</p>
        <p>Using the collected stories and testimonies from around the world, I create pictures, prints and art installations etc.</p>
