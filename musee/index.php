        <h1>Créez votre retable</h1>
        <p>Dans les églises fribourgeoises, en-dessus des autels, on trouvait de magnifiques retables. Un retable est une sorte d'armoire à deux volets avec des sculptures ou des peintures.
            Durant l'année, on ouvrait ou on fermait les volets, selon les fêtes religieuses. Ainsi, les fidèles voyaient toujours quelque chose de différent: à l'époque on n'avait pas de télévision ni de journaux - cela rendait leurs venues à l'église captivantes!
            Les retables étaient faits sur commande. Les commanditaires (clients des sculpteurs) étaient les paroisses, les corporations (groupes de métiers, comme par exemple les bouchers, les forgerons) mais aussi les privés (nobles, seigneurs). Les sculpteurs travaillaient à partir de modèles (qui constituaient d'une certaine manière un catalogue) à partir desquels les commanditaires choisissaient quels saints représenter dans la caisse, quelles scènes sur les volets, ainsi que leurs couleurs.
