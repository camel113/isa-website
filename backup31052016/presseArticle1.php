        <p class="presseTxt">Isabelle Pilloud, une Fribourgeoise de 42 ans qui a quitté la Suisse en 1996, vit à Berlin, cette ville d’Europe qui lui a octroyé un atelier-appartement dans le cadre d’un programme de soutien aux artistes. Elle y respire en deux temps, entre son travail de guide et sa peinture. Le double est riche pour celle qui, en plus, aime la ville autant que la campagne, et qui revient souvent à Fribourg et dans le Jura. De la grande capitale allemande - ville très étalée qui mêle curieusement de vastes étendues de verdure aux attributs trépidants et complexes des plus grandes cités – elle ramène une exposition au questionnement intense et dont la démarche est axée essentiellement sur l’observation des femmes. Des femmes qui attendent.</p>
