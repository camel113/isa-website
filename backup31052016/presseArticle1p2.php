        <h3>Une portraitiste incisive</h3>
        <p class="presseTxt">Isabelle Pilloud n’est pas une inconnue à la Galerie du Soleil puisqu’elle y a participé plusieurs fois à des expositions collectives. C’est aujourd’hui une première pour cette jeune femme que d’occuper les cimaises à elle seule et l’initiative des galeristes est excellente car elle permet de découvrir une artiste peintre à part entière. Découverte qui avait déjà été possible en 2003, lors de sa première grande exposition personnelle au Château de Saint-Aubin. Nous l’avions auparavant connue, au fil des années, comme l’enseignante des arts visuels qu’elle fut à l’Ecole professionnelle du Canton de Fribourg, comme une élève du peintre jurassien Yves Voirol (lorsqu’il enseignait à l’Universtité de Berne), comme une portraitiste incisive qui extirpe non pas des douceurs flatteuses pour charmer ceux et celles qu’elle croque, mais qui soutire du tréfonds des êtres posant en face d’elle de l’expression à l’état brut, sans concession. Les autoportraits étaient tout aussi peu aimables et même assez féroces.</p>
        <p class="presseTxt">Dans l’exposition du Soleil, le tableau intitulé Alexanderplatz (autoportrait) arbore un personnage qui devient un objet empreint de maturité, au regard perçant, droit, à la posture déterminée du dramaturge viril bien campé sur ses tréteaux. Ce personnage de femme fixant le spectateur avec insistance fait vibrer le tableau qui vit tout seul par lui-même, dans une mise-en-scène portant le thème central de l’exposition : « l’attente entre deux mondes », au sein d’un décor que l’on retrouve dans la plupart des tableaux de l’expo, c’est-à-dire une gare, une rue, une station, un trottoir, un bistrot, un bus, un banc sur un quai… un métro.</p>
