        <h2>Isabelle Pilloud peint une ode à la femme</h2>
        <h3>Exposition. Le château de Saint-Aubin montre une cinquantaine de peintures et de gravures de l’artiste fribourgeoise. Pour l’accrochage actuel, ce sont les femmes de Berlin, souvent inconnues, qui tiennent la vedette.</h3>
        <p class="presseTxt">Le chant d’investigation d’Isabelle Pilloud est infini. Les rues de Berlin, où l’artiste fribourgeoise vit, et les femmes, les inconnues qu’elle croise ou qu’elle rencontre dans cette grande ville. Saint-Aubin, son village d’origine, consacre une exposition à l’œuvre d’Isabelle Pilloud.</p>
        <p class="presseTxt">L’artiste-peintre et graveuse montre une cinquantaine d’œuvres. Celles-ci révèlent combien la femme intéresse Isabelle Pilloud. « C’est mon terrain depuis environ dix ans. Actuellement, ce sont les femmes de Berlin que je peins puisque j’y vis depuis 1996. D’une part je suis attirée par la grande ville et son animation grouillante et les femmes que je vois dans cette ville. Je me demande quelles sont leurs conditions de vie. Avec les inconnues, notamment les femmes de la communauté turque, souvent inapprochables, je me demande ce qu’elles vivent dans une société émancipée ! J’ai beaucoup de questions qui s’alimentent de la richesse et de la diversité de la grande ville. »</p>
