        <p class="presseTxt">En regardant les femmes, l’artiste peintre se fait spectatrice puis passeuse d’images sur les actrices-passantes. Utilisant ses propres attentes, la peintre fait des petits croquis qu’elle met ensuite au repos, accrochés au mur de son atelier. Le travail plus conséquent vient ensuite. Quelques-uns, retouchés à l’aquarelle, sont exposés et intitulés <em>Sur le vif.</em></p>
