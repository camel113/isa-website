        <p class="presseTxt">Un métro qui est jaune. Le jaune, une couleur phare d’ailleurs, puisqu’elle définit souvent l’objet au centre d’un tableau plutôt sombre, mettant en exergue une histoire. Ou alors, ce jaune, on est dedans, baignés par une lumière d’or, comme dans la Scène de métro à Kreuzberg, avec au premier plan une madone musulmane, emmitouflée, enfermée dans un voile-manteau noir, mais avec au second plan, deux femmes au visage découvert qui sourient et se parlent. Deux situations différentes, aux mouvements contraires, glissant l’une sur l’autre en parallèle, dont la deuxième (joyeuse, ouverte, en arrêt) apporte un antidote à la première (hermétique, furtive, muette, porteuse de choses contraintes).</p>
