        <p class="presseTxt">Malgré elle, peut-être, Isabelle Pilloud approche et peint la femme avec sa sensibilité féminine. Ses modèles féminins sont jeunes ou vieux, proches ou inconnus. Fragiles et émouvants dans leur simplicité et leur solitude. A travers l’huile ou la gravure, elle dialogue en couleurs avec ses modèles, qui lui renvoient l’image de la femme et de sa place dans la société. Le soir, l’attente dans un bar, devant ou face à l’alignement des bouteilles avec, ici ou là, un groupe de musiciens. L’ambiance de la danse, de la vie qui passe, mais aussi de la terrible solitude que génère le monde urbain. Quelques triptyques racontent ces bistrots où les rencontres sont éphémères et où les chopes de bière défilent.</p>
        <p class="presseTxt">Sa quête de l’intimité féminine, Isabelle Pilloud la poursuit dans une installation dans le caveau du château. L’espace voûté lui a inspiré une démarche nouvelle. Sur des panneaux de soie blanche, elle accroche une série de huit femmes, assorties de moulages et collages. En suspension et en transparence, ces sérigraphies habitent le caveau.</p>
        <p class="presseTxt">Enfant de Saint-Aubin, Isabelle Pilloud a enseigné les arts plastiques dans le canton de Fribourg avant de partir à Berlin. Tout d’abord pour un an, puis pour un séjour qui se prolonge. Elle a obtenu en 2002 un atelier-appartement de la ville de Berlin dans le cadre du programme de soutien aux créateurs. Sa dernière exposition importante en Suisse date de 1999 à Langnau. Au château de Saint-Aubin, Isabelle Pilloud partage ses « Regards de femmes » jusqu’au 19 avril 2003.</p>
