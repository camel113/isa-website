        <p>Gleichzeitig habe ich für unsere Heldinnen, diese ausserordentlichen Frauen, auf einer grauen rohen Leinwand eine Weltkarte gestickt. Diese Karte wird an verschiedene Orte wandern. Besucher werden dazu eingeladen, eine Perle an den Ort ihrer Heldin anzunähen. (Falls Sie nicht vor Ort sein können, werde ich gerne die Perle laut Ihren Anweisungen an die von ihnen gewünschte Stelle annähen.)</p>
